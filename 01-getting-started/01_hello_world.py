"""
Hello World - Your First Python Program

This is the traditional first program that every programmer writes.
It's simple but important - it confirms that Python is working correctly!
"""

# The simplest Python program
print("Hello, World!")

# You can also print multiple things
print("Hello,", "World!")

# Print with different quotes
print('Hello, World!')

# Print numbers
print(42)

# Print multiple lines
print("Hello,")
print("World!")

# Print with special characters
print("Hello, World!\nThis is a new line.")

# Print an empty line
print()
print("Above is an empty line")

