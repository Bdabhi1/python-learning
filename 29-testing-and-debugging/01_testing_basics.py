# 1. WHAT IS TESTING?
# ============================================================================
print("=" * 60)
print("1. WHAT IS TESTING?")
print("=" * 60)

print("  Testing is the process of verifying that code works correctly.")
print("  ")
print("  Types of tests:")
print("    - Unit tests: Test individual functions/methods")
print("    - Integration tests: Test how components work together")
print("    - System tests: Test the entire system")
print("  ")
print("  Benefits:")
print("    - Catch bugs early")
print("    - Documentation (tests show how code works)")
print("    - Refactoring confidence")
print("    - Prevent regressions")

