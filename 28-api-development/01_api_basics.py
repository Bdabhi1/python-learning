# ============================================================================
# 1. WHAT IS AN API?
# ============================================================================
print("=" * 60)
print("1. WHAT IS AN API?")
print("=" * 60)

print("  API (Application Programming Interface) is:")
print("    - A contract between applications")
print("    - A way for applications to communicate")
print("    - Endpoints that accept requests and return responses")
print("    - Stateless communication")
print("  ")
print("  Types of APIs:")
print("    - REST API: Most common, uses HTTP methods")
print("    - GraphQL: Query language for APIs")
print("    - SOAP: XML-based protocol")

print()  # Empty line


# ============================================================================
# 2. REST API PRINCIPLES
# ============================================================================
print("=" * 60)
print("2. REST API PRINCIPLES")
print("=" * 60)

print("  REST (Representational State Transfer):")
print("    - Uses HTTP methods (GET, POST, PUT, DELETE)")
print("    - Returns JSON (usually)")
print("    - Stateless (each request is independent)")
print("    - Resource-based URLs")
print("    - Standard status codes")

print()  # Empty line


# ============================================================================
# 3. HTTP METHODS
# ============================================================================
print("=" * 60)
print("3. HTTP METHODS")
print("=" * 60)

print("  Common HTTP methods:")
print("    - GET: Retrieve data")
print("    - POST: Create new resource")
print("    - PUT: Update entire resource")
print("    - PATCH: Partial update")
print("    - DELETE: Delete resource")

print()  # Empty line


# ============================================================================
# 4. HTTP STATUS CODES
# ============================================================================
print("=" * 60)
print("4. HTTP STATUS CODES")
print("=" * 60)

print("  Common status codes:")
print("    - 200: OK (success)")
print("    - 201: Created")
print("    - 400: Bad Request")
print("    - 404: Not Found")
print("    - 500: Internal Server Error")

print()  # Empty line


# ============================================================================
# 5. API ENDPOINTS
# ============================================================================
print("=" * 60)
print("5. API ENDPOINTS")
print("=" * 60)

print("  Example endpoints:")
print("    GET    /api/users          # Get all users")
print("    GET    /api/users/1       # Get user with id=1")
print("    POST   /api/users         # Create new user")
print("    PUT    /api/users/1       # Update user with id=1")
print("    DELETE /api/users/1       # Delete user with id=1")

print()  # Empty line


